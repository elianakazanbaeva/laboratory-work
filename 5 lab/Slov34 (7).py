def shows_time(day, start, end):
    """Возвращает передачи в указанный день в заданном временном промежутке"""
    if day not in tv_schedule:
        print('День', day, 'не найден в расписании')
        return []
    for time in tv_schedule[day]:
        if start <= time <= end:
            shows = tv_schedule[day][time]['название']
    result = []
    for time, shows in tv_schedule[day].items():
        if start <= time <= end:
            for show in shows:
                result.append({
                    'время': time,
                    'название': show['название'],
                    'канал': show['канал'],
                    'вид': show['вид']
                })

    return result


